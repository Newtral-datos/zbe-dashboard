// Función para calcular vehículos afectados según tipo de restricción
export const calculateAffectedVehicles = (city) => {
  if (!city.vehicleData) return 0;
  
  const restrictions = city.restrictedVehicles.toLowerCase();
  let affected = 0;
  
  if (restrictions.includes('sin etiqueta') || restrictions.includes('sin distintivo')) {
    affected += city.vehicleData.sinDistintivo;
  }
  if (restrictions.includes('etiqueta b') || restrictions.includes('distintivo b')) {
    affected += city.vehicleData.distintivoB;
  }
  if (restrictions.includes('etiqueta c') || restrictions.includes('distintivo c')) {
    affected += city.vehicleData.distintivoC;
  }
  
  return affected;
};

// Estadísticas generales
export const getGeneralStats = () => {
  const totalCities = zbeData.length;
  const totalAffected = zbeData.reduce((sum, city) => sum + (city.affectedVehicles || 0), 0);
  const totalVehicles = zbeData.reduce((sum, city) => {
    return sum + (city.vehicleData?.totalVehicles || 0);
  }, 0);
  const totalSurface = zbeData.reduce((sum, city) => {
    const surface = parseFloat(city.surface);
    return sum + (isNaN(surface) ? 0 : surface);
  }, 0);
  
  return {
    totalCities,
    totalAffected,
    totalVehicles,
    totalSurface: totalSurface.toFixed(2),
    affectedPercentage: ((totalAffected / totalVehicles) * 100).toFixed(2)
  };
};

